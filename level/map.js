let map = [];
function createMap() {
  const mapSize = 17;
  const mapCenter = Math.floor(mapSize / 2);
  for (let i = 0; i < mapSize; i++) {
    map.push([]);
  }
  for (let row = 0; row < mapSize; row++) {
    for (let column = 0; column < mapSize; column++) {
      map[row].push("0");
    }
  }
  map[mapCenter][mapCenter] = "1";
}
