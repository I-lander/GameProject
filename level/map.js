let map = [];
function createMap() {
  const mapSizeX = 17;
  const mapSizeY = 17;
  const mapCenterX = Math.floor(mapSizeX / 2);
  const mapCenterY = Math.floor(mapSizeY/2);
  for (let i = 0; i < mapSizeX; i++) {
    map.push([]);
  }
  for (let row = 0; row < mapSizeX; row++) {
    for (let column = 0; column < mapSizeY; column++) {
      map[row].push("0");
    }
  }
  map[mapCenterX][mapCenterY] = "1";
}
